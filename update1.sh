#!/bin/bash

# Универсальный скрипт обновления бота
# Запускать из корня папки бота (например ~/bots/bot1)

echo "--- Обновление [ESIM-BOT-1] ---"

# 1. Стянуть свежий код
echo "1. Загрузка кода из репозитория Bot 1..."
git pull

# 2. Обновить зависимости в папке бота
echo "2. Проверка зависимостей (bot)..."
cd bot
npm install

# 3. Перезапустить процесс в PM2
echo "3. Перезапуск процесса [esim-bot-1]..."
pm2 restart esim-bot-1

echo "--- Готово! eSIM-BOT-1 обновлен. ---"
pm2 status esim-bot-1
